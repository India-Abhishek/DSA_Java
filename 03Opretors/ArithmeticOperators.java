/* Arithmetic Operators used to do opration between two numbers. */

public class ArithmeticOperators {
    public static void main(String args[]) {

        System.out.println(9 + 5); // will add both number.
        System.out.println(9 - 5); // will give the diffrence between two numbers.
        System.out.println(9 * 5); // will multiply both.
        System.out.println(9 / 5); // will divide and give u the integer value *coz we are dividing an integer with an integer* 
                                   // and it will not give u a round of value like 1.8= 2 NO!!! it will give THE 1 (one).

        System.out.println(9 % 5); // will give u the reminder.

        System.out.println('a' + 5); // will add the 5 with the ascii value of "a" which is 97.

        System.out.println('A' + 5); // will add the 5 with the ascii value of "a" which is 65.

        System.out.println('a' + 'b'); // will add the ascii value of both "a and b" which is 97 and 98.        
    }
}
